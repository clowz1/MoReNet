    radius = 0.005  # 圆的半径
    u = np.linspace(0, 2 * np.pi, 100)
    v = np.linspace(0, np.pi, 100)
    x_circle = radius * np.outer(np.cos(u), np.sin(v)) + x
    y_circle = radius * np.outer(np.sin(u), np.sin(v)) + y
    z_circle = radius * np.outer(np.ones(np.size(u)), np.cos(v)) + z

